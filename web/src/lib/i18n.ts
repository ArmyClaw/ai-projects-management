const applyLang = () => {
  document.documentElement.lang = localeRef.value;
};

export const setLocale = (next: Locale) => {
  localeRef.value = next;
  localStorage.setItem(STORAGE_KEY, next);
  applyLang();
};

export const t = (key: string) => messages[localeRef.value][key] ?? key;

export const useI18n = () => ({
  locale: computed(() => localeRef.value),
  isZh: computed(() => localeRef.value === "zh-CN"),
  setLocale,
  t,
});

applyLang();
